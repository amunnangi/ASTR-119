# Assignment operators
x = 9       # set x = 9
x += 3      # x = x + 3
print(x)
x = 9       
x -= 3      # x = x - 3
print(x)
x *= 3      # x = x * 3
print(x)
x /= 3      # x = x / 3
print(x)
x **=3      # x = x ** 3
print(x)

# Comparison operators
x = 9
y = 3
print(x==y)     #True if x equals y, False otherwise
print(x!=y)     #True if x does not equals y, False otherwise
print(x>y)     #True if x is greater than y, False otherwise
print(x<y)     #True if x less than y, False otherwise
print(x>=y)     #True if x greater than or equals y, False otherwise
print(x<=y)     #True if x less than or equals y, False otherwise