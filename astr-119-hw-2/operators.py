x = 9
y = 3

# Arithmetic Operators
print(x+y)  #Addition
print(x-y)  #Subtraction
print(x*y)  #Multiplication
print(x/y)  #Division
print(x%y)  #Modulus gives remainder of division
print(x**y) #Exponentiation
x = 9.191823
